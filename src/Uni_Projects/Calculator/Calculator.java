package Uni_Projects.Calculator;

public class Calculator {
    int sum(int a, int b) {
    int result = a + b;
    return result;
}

    int minus(int a, int b) {
        int result = a - b;
        return result;
    }

    int multiply(int a, int b) {
        int result = a * b;
        return result;
    }

    int divide(int a, int b) {
        int result = a / b;
        return result;
    }

    int secret(int a, int b) {
        int result = a + b - a;
        return result;
    }
}
